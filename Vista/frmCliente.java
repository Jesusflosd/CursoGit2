   public void inicializarTablaClientes() {
        DefaultTableModel titulos = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int filas, int columnas) {
                return false;
            }
        };

        titulos.addColumn("ID Cliente");
        titulos.addColumn("Nombre");
        titulos.addColumn("Apellido Materno");
        titulos.addColumn("Apellido Paterno");


        List<String> data = csv.obtenerDatosArchivo(fileName);
        if (data != null) {
            for (String line : data) {
                String[] row = line.split(",");
                if (row.length >= 4) {
                    Object[] fila = new Object[4];
                    fila[0] = row[0];
                    fila[1] = row[1];
                    fila[2] = row[2];
                    fila[3] = row[3];
                   
                    titulos.addRow(fila);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error al leer el archivo");
        }

        tabla_consultaClientes.setModel(titulos);
        tabla_consultaClientes.setRowHeight(60);
    }
 

