    private void tabla_consultaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_consultaClientesMouseClicked
        
        int mouseClick = tabla_consultaClientes.rowAtPoint(evt.getPoint());

        int idCliente = Integer.parseInt((String) tabla_consultaClientes.getValueAt(mouseClick, 0));
        String nombre = "" + tabla_consultaClientes.getValueAt(mouseClick, 1);
        String apellidoPaterno = "" + tabla_consultaClientes.getValueAt(mouseClick, 2);
        String apellidoMaterno = "" + tabla_consultaClientes.getValueAt(mouseClick, 3);
        
        texto_ID.setText(String.valueOf(idCliente));
        texto_Nombre.setText(nombre);
        texto_ApellidoPaterno.setText(apellidoPaterno);
        texto_ApellidoMaterno.setText(apellidoMaterno);
        
    }//GEN-LAST:event_tabla_consultaClientesMouseClicked

    public void eliminarCliente() {
        try {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estas seguro de eliminar al cliente?", "Aviso", 0);
            if (confirmacion == 0) {
                metodosCliente.eliminarDatosPersona(Integer.parseInt(texto_ID.getText()));
                inicializarTablaClientes();
          
            }
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, exception);
        }
    }
    
